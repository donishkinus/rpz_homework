string = '21f.d/df/ss56s.ghs22s3'

d = {'Спец.символи': 0, 'Букви': 0, 'Цифри': 0}
for i in string:
    if i.isalpha():
        d['Букви'] += 1
    elif i.isdigit():
        d['Цифри'] += 1
    else:
        d['Спец.символи'] += 1

print('Цифри = ',d['Цифри'],'|Букви = ' ,d['Букви'],'|Спец.символи = ' ,d['Спец.символи'])